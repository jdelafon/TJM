<h3>Le marché de Noël - Caux</h3><br />
A Montreux, les berges du lac Léman accueillent chaque année du 26 novembre au 24 décembre 
le plus fameux marché de Noël de Suisse. Dans des chalets typiques décorés de guirlandes, des 
milliers d'idées de cadeaux pour les fêtes de fin d'année, des dégustations de l'art culinaire 
de divers pays et des propgrammes culturels. Le tout dans une atmosphère chaleureuse et festive.

<br /><br />
