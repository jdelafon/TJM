Ne laissez pas passer l'occasion de rencontrer le Père Noël en personne! Pour cela, il faudra 
escalader en train les Rochers de Naye; son chalet de vacances et son bureau se trouvent au sommet. 
Il vous accueillera volontiers, vous et vos enfants. Et si vous décidez de profiter de son hospitalité, 
vous pourrez passer la nuit dans l'une de ses yourtes. <br /><br />
Durant les jours fériés de fin d'année, le Château de Chillon est aussi le théâtre du Festival des 
Contes et du Marché Médiéval: personnages de contes et costumes d'époque vous attendent dans l'atmosphère 
d'un véritable château du moyen-âge.

