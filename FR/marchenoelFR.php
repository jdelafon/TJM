<br /><br />

Nous vous proposons une promenade inoubliable à Caux, un village d'altitude se trouvant au-dessus 
de la ville de Montreux et offrant un panorama grandiose des Alpes au-dessus du lac Léman. On y trouve 
également de nombreux jeux et activités pour les enfants, et les animaux compagnons du Père Noël! 
Aux adultes, on proposera plutôt du vin chaud à la cannelle. <br /><br />

