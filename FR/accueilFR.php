<h3>Pourquoi TJM?</h3><br />

TJM, c'est la possibilité de découvrir la Suisse de façon individuelle et personnalisée, grâce à des 
accompagnants-interprètes qui ont toujours vécu dans notre région et sauront éviter les clichés pour 
vous montrer la culture de notre pays.<br />
Nous proposons à nos clients des excursions riches et culturelles, mais leur permettons aussi de choisir eux-mêmes 
ce qu'ils désirent visiter, dans un encadrement tout aussi professionnel.<br />
Nous possédons de nombreux contacts en Russie et au Kighizstan, garantissons une qualité de service suisse, 
et pratiquons des tarifs avantageux.<br /><br /><br />

