Au nord, un plateau parsemé de collines; on y trouve la capitale, Berne, mais aussi Zurich ou Lucerne. 
Au centre, à l'est et au sud, les hauts sommets des Alpes et du Jura, neiges éternelles et paisibles 
vallées creusées au cours des siècles par les glaciers. A l'extrémité ouest, le Rhône se jette dans le lac 
Léman, avant de continuer sa course à travers Genève, centre économique européen, vers la France voisine. 
La culture suisse est indissociable de ses montagnes qui recouvrent les deux tiers de sa surface.
