Se trouvant au centre des Alpes, la Suisse comprend plus de 140 stations de ski. Les amateurs de retraite 
paisible préféreront les croisières sur le lac, les randonnées en montagne, golf, bain de soleil sur un glacier 
ou bain thermal dans des eaux minérales naturelles. La petite taille du pays permet de voyager rapidement 
d'une région à l'autre et entre les villes, grâce à un excellent réseau ferroviaire - la traversée du pays 
prend environ 4 heures.<br />
La Suisse est le pays de l'air pur, de la sérénité et du cor des Alpes, instrument profond et unique, 
symbole de notre musique traditionnelle.
