	<h2>La Suisse</h2>

   	<div class="inblock">

La Suisse est située au centre de l'Europe. Elle est constituée de 26 cantons parlant l'une des quatre langues 
officielles: l'allemand (70% environ), le français (20%), l'italien (10%) et le romanche (très peu). 
Bien qu'elle ne recouvre qu'environ 41'000 km<sup>2</sup>, sa population est un peu supérieure à 7 millions 
de personnes. Elle partage ses frontières avec la France, l'Allemagne, l'Italie, l'Autriche et le 
Lichtenschtein. 
