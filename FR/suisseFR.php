Une grande fierté de la Suisse sont ses lacs offrant des paysages de carte postale. Les plus grands sont ceux de 
Genève (le Léman), Zurich, Neuchâtel, Lugano, et celui des Quatre-Cantons. Leur profondeur et la pureté de leurs 
eaux leur confèrent une couleur particulière. Leurs berges entourées de collines vertes, de falaises ou de 
pentes plantées de vignoble sont des lieux de pèlerinage pour les touristes.
