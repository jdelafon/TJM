<h3>Glaciers du Rhône – route de Souvorov - pont du Diable</h3><br />

Depuis Montreux, nous traverserons en voiture tout le canton du Valais, ses vallées, ses vignobles et ses champs 
d'abricotiers. Nous remonterons le Rhône jusqu'à sa source, des glaciers que nous pourrons visiter de l'intérieur.
<br /><br />

<img src='../images/glacier0.jpg' alt='glacier' /><br /><br />

Ensuite, nous continuerons le voyage à travers le col de la Furka (2400 mètres d'altitude), pour 
visiter la légendaire route de Souvorov à travers les Alpes. Vous y verrez le fameux pont du Diable, 
apprendrez l'histoire de son constructeur, pourrez juger de l'art tactique de A.V.Souvorov et admirer 
l'héroisme des soldats russes dans leur guerre de 1799 pour gagner le pont. A côté du pont du Diable, 
une croix de 12 mètres de haut est taillée dans la falaise en mémoire des victimes.<br />
Pour le retour, nous installerons la voiture sur un wagon du train qui rejoint 
Oberwald par un tunnel dans la montagne, puis continuerons vers Montreux pour terminer le voyage.

