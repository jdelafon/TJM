Находясь в самом центра Альп, Швейцария распологает более чем 140 лыжными центрами. Горное 
расположение определяет и виды программ, предлагаемых туристам. Для любителей спокойного отдыха 
– это озерные круизы, поездки по горной железной дороге, пешеходные прогулки, гольф, солнечные 
ванны на леднике. Небольшие размеры Швейцарии позволяют совершать однодневные поездки в соседние 
города: Берн, Цюрих, Женеву, Лозанну.<br />
Швейцария – страна целебных вод, чистого воздуха и зимних курортов. Высоко ценится среди туристов 
и летняя Швейцария, когда вечерами в большинстве швейцарских отелей играет живая музыка. И в каждом 
городе существует фольклорный ресторан, в котором турист может не только послушать тирольские 
мелодии, но и сам извлечь какой-нибудь звук из альпийского рога.
