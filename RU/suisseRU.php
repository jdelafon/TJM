Гордость Швейцарии – ее живописные озера. Самые крупные из них – Женевское и Боденское, затем 
Невшательское, Лаго-Маджоре, Озеро четырех кантонов, Цюрихское, Лугано. Из-за большой глубины 
и прозрачности воды, озера имеют голубой или синий цвет. Берега окаймлены лесистыми холмами или 
скалистыми горами, склоны которых обрываются прямо в воду. Крупные озера – места паломничества 
туристов.
