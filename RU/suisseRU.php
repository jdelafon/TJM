	<h2>О Швейцарии</h2>

   	<div class="inblock">

Швейцария расположена в центре Европы. Ее площадь – всего 41,3 тыс. кв. км., а 
население насчитывает немногим более 6,99 млн. человек. Столица – город Берн. 
Три четверти ее границ – с Францией. Австрией и Италией – проходят по высоким горным 
хребтам Юры и Альп, и лишь граница с Германией Лихтенштейном идет отчасти по низменности 
– долине Рейна. 
