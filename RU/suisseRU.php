Север страны – холмистое плато. Здесь находятся такие крупные промышленные центры, как Цюрих. 
Центральная часть, восток и юг – горы, глубокие ущелья, причудливые утесы и скалы, вечные снега 
и ледники. Берега Женевского озера, а также долина Вале, где протекает река Рона, - это район 
садов, полей и виноградников. На границе Швейцарии, там, где Рона уходит во Францию, расположена 
Женева. Понятие Швейцария и горы неотделимы друг от друга. Около 2/3 площади страны занято горами. 
Выше 2500 – 3000 м. снег не тает в течении всего года.
