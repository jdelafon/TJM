<h3>Ледники Рона – Дорога Суворова - Чертов Мост</h3><br />

Из Монтре на автомобиле Вы пересечете весь кантон Вале, увидете красивейшие горные перевалы, 
знаменитые виноградники, абрикосовые сады. Поднимитесь вдоль реки Рон ( Рон – это река, которая 
впадает в Женевское озеро, в последствии пересекает всю Францию и заканчивает свой путь в 
Средиземном Море возле Марселя) до самого ее начала, до ледников, которые вы сможете увидеть изнутри.<br /><br />

<img src='../images/glacier0.jpg' alt='glacier' /><br /><br />

После чего Вы продолжите путешествие через перевал Фурка (2400 метров над уровнем моря). Вам 
предстоит уникальная возможность посетить места легендарного похода Суворова через Альпы. Вы 
увидите знаменитый Чертов Мост, познакомитесь с историей его строительства, оцените тактическое 
искусство А. В. Суворова и восхититесь героизмом русских воинов, с боем прошедших по мосту в 
1799 году. Рядом с Чертовым Мостом, в память о погибших при переходе через Альпы солдатах, в 
скале высечен 12-метровый православный крест и установлена мемориальная доска.<br />

Вернуться в Монтре Вам предстоит через типичный Швейцарии высокогорный городок Андермат, где 
мы поставим наш автомобиль на прицеп поезда и по горной тюннеле доедем до Обервальда, отсюда 
на нашем автомобиле вернемся в Монтре.

