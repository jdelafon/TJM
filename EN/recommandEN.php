<h3>Визит винодельни</h3><br />

Находясь в Швейцарии не упустите возможность оценить местное вино. Вы убедитесь в том, 
что по своему качеству оно нисколько не уступает французским и итальянским винам. Мы 
организуем Вам визит одной из наилучшей семейной винодельни. Здесь Вы увидете процесс 
изготовления вина, продигустируете его, сможете задать вопросы опытному виноделу, совершите 
прогулку по виноградным плантациям. По желанию сможете приобрести понравившееся Вам вино.
