<h3>Полёт на парапенте/на верталёте</h3><br />

Если Вы любитель острых ощущений и не боитесь высоты, то полет на парапенте – это для Вас! 
Для этого не нужно никакой специальной подготовки. Полет происходит в паре с инструктором, 
Вы должны внимательно слушать и выполнять его инструкции, ну и конечно же наслаждаться полетом 
и любоваться красотами Швейцарии с высоты птичьего полета.<br />
Стоимость полета на одного человека – 150 FR (трансфер оплачивается дополнительно).<br /><br />

