Because it is in the middle of the Alps, Switzerland owns more than 140 ski stations. Quiet people would 
prefer cruises on the lake, hikes on the mountain, golf, sunbath on a glacier or warm bath in a thermal 
station with natural mineral water. Since the country is very small, it is easy to travel from one side 
of the country to another, thanks to a very performant train network (it takes around 4 hours to cross 
the country).<br />
In summer evenings, music is played in most of the hotels, and in every city you will find at least one 
folk restaurant where you can hear our traditional music while tasting swiss food.
