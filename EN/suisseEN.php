Switzerland is situated in the center of Europe. It is made of 26 cantons, each of them speaking one of 
the four official languages: german, french, italian and romanche. Although it is only 41'000 km<sup>2</sup> 
wide, its population is a bit more than 7 million people. It shares frontiers with France, Germany, Italy, 
Austria and Liechtenschtein. 
