In the north, a wide plateau covered by hills; there is the capital, Bern, but also Zurich and Lucerne. 
In the middle, in the east and in the south, the high peaks of the Alps and of the Jura, eternal snow and 
quiet valleys digged by glaciers. In the western region, the river Rhone throws itself into the lake Leman, 
then continues across Geneva, european economic center, to France. Swiss culture is very close to the 
mountains, which cover two thirds of its surface.
