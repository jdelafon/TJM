Switzerland is particularly proud of its lakes that offer magnificent landscapes. The biggests of them are 
Leman-, Zurich-, Neuchatel-, Lugano- and Four-Cantons- Lakes. Depth and purity of their waters give them a 
special tone of blue. Their banks are surrounded by hills, forests, cliffs or slopes with vineyards; they 
are tourists' favorite places.
